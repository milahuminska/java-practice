package home_practice;

public class MyPlusplus_review {
	public static void main(String[] args) {
		
		int n1  = 2;
		int n2 = 7;
		int n3 = ++n2 + n1++; //8 + 2 
		System.out.println(n3);//10
		
		int n4 = 20;
		int total = --n4 - n1 + n2; // 19 - 3 + 8 
		System.out.println(total);//24
		
		
		
		
		
	}

}
