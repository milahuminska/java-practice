package home_practice;

public class ArraysPractice {
	public static void main(String[] args) {
		int[] nums = { 1, 4 };
		System.out.println("Length: " + nums.length);

		for (int i = 0; i < nums.length; i++) {
			System.out.print(nums[i]+" ");
		}
		
//		i = 0 | 0 < nums.length | println(nums[0])
//		i++ | i =1 | 1 < nums.length| println(nums[1])
//		i++ | i =2 | 2 < nums.length, break. 
//		int i = 0 | when to start 
//		i < nums.length | until what time to do
//		i++ | update every iteration
//		{ } | whatever inside curly braces
//		we will be repeated with updated i index
	}
}
