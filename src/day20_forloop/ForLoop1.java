package day20_forloop;

public class ForLoop1 {
	public static void main(String[] args) throws InterruptedException {
		// using for loop print "Love Java!" 10 time

		for (int i = 0; i < 10; i++) {
			System.out.println("Love Java!");
			// for (int i = 1; i <= 10; i ++) {
}

		// print numbers from 5 to 15 using a for loop
		
		
		for (int i = 5; i <= 15; i++ ) {
			System.out.println(i);
		}
		System.out.println("===============================");
		
		// For loop 20 - 10
		
		for(int i = 20; i >= 10; i--) {
			System.out.print(i+" ");
			Thread.sleep(456);
		}
		
		
		
		
	}
}
