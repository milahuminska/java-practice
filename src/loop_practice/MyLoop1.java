package loop_practice;

public class MyLoop1 {
	public static void main(String[] args) {
		//step 1 --> initialization int i=0
		//step 2 --> check the condition i<1
		//step 3 --> print "hello "+i
		//step 4 --> increment i++;
		//step 5 --> back to step 2 
		
		for (int i = 0; i < 5; i++) {
			System.out.println("hello "+i);
		}

	}
}
