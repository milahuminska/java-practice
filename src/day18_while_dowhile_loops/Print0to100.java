package day18_while_dowhile_loops;

public class Print0to100 {
	public static void main(String[] args) {
		// 0 ------ 100
		int num = 0;
		    // 101
		 while(num<=100){
	    	
	      System.out.println(num);// ("Hi")
	      num++; // num = num+1
	    }
		 						        //101
	    System.out.println("Counter: "+ num);
	   
	    
	    
	    
	    
	}

}
